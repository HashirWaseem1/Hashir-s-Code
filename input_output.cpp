#include<iostream>
using namespace std;
int main(){
    // '<<' This is called insertion.
    // '>>' This is called Extraction.
    int num1,num2;
    //input value of num1.
    cout<<"Enter the value of num1: \n";
    cin>>num1;
    //input value of num2.
    cout<<"Enter the value of num2: \n";
    cin>>num2;
    // for output.
    cout<<"The sum is" << num1+num2;
    return 0;
}